// Arreglos:

let frutas = ["melon", "manzana"];
console.log(frutas);

// Push añade elementos al final del arreglo
frutas.push("Sandia");
console.log(frutas);

// Unshift añade un elemento al principio del arreglo
frutas.unshift("Pera");
console.log(frutas);

// Eliminar  el ultimo elemento del arreglo
let eliminafruta = frutas.pop();
console.log(frutas);
console.log(eliminafruta);

// Shit elimina el primer elemento del arreglo
let eliminafrutaI = frutas.shift();
console.log(frutas);
console.log(eliminafrutaI);